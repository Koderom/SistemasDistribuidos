    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        String nick = this.nick.getText();
        String password = String.valueOf(this.password.getPassword());
        String respuesta = this.parent.loggearUsuario(nick, password);
        HashMap<String, String> info = (HashMap<String, String>) Parse.convertMessageToInfo(respuesta);
        if(info.containsKey("COD") && info.get("COD").equals("200")){
            this.setVisible(false);
            this.parent.loginExitoso();
        }else this.errorMessage.setText(info.get("MSJ"));
    }//GEN-LAST:event_loginActionPerformed

