    private void boton_registrarseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_registrarseActionPerformed
        String nick = input_nick.getText();
        String password = String.valueOf(input_password.getPassword());
        String respuesta = this.parent.registrarUsuario(nick, password);
        HashMap<String, String> info = (HashMap<String, String>) Parse.convertMessageToInfo(respuesta);
        if(info.containsKey("COD") && info.get("COD").equals("200")){
            this.setVisible(false);
            this.parent.loginExitoso();
        }else text_message.setText(info.get("MSJ"));
    }//GEN-LAST:event_boton_registrarseActionPerformed

